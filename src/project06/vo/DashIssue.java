package project06.vo;

public class DashIssue {
	private int ino;
	private int solve;
	private String writer;
	private int wdate;
	public int getIno() {
		return ino;
	}
	public void setIno(int ino) {
		this.ino = ino;
	}
	public int getSolve() {
		return solve;
	}
	public void setSolve(int solve) {
		this.solve = solve;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public int getWdate() {
		return wdate;
	}
	public void setWdate(int wdate) {
		this.wdate = wdate;
	}
	
}
