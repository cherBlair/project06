package project06.vo;

public class DashRe {
	private int rno;
	private int ino;
	private String writer;
	private int wdate;
	
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public int getIno() {
		return ino;
	}
	public void setIno(int ino) {
		this.ino = ino;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public int getWdate() {
		return wdate;
	}
	public void setWdate(int wdate) {
		this.wdate = wdate;
	}
	
}
